package debyanshu;

public class Arrays {
    public static void main(String[] args) {
        int a[]={1,7,8,9,10,12,45};
        number(a);
    }
    static void number(int a[]){
        for (int b:a){
            System.out.print(b+" ");
        }
      //  System.out.println();
    }
}
